package com.aims.info.staffconfiguration;

public class StaffDiscipline {
	private Integer disciplineCd=null;
	private String disciplineNm;
	private String disciplineDesc;
	private Integer staffTypeCd=null;
	private String status;
	/**
	 * @return Returns the disciplineCd.
	 */
	public Integer getDisciplineCd() {
		return disciplineCd;
	}
	/**
	 * @param disciplineCd The disciplineCd to set.
	 */
	public void setDisciplineCd(Integer disciplineCd) {
		this.disciplineCd = disciplineCd;
	}
	/**
	 * @return Returns the disciplineDesc.
	 */
	public String getDisciplineDesc() {
		return disciplineDesc;
	}
	/**
	 * @param disciplineDesc The disciplineDesc to set.
	 */
	public void setDisciplineDesc(String disciplineDesc) {
		this.disciplineDesc = disciplineDesc;
	}
	/**
	 * @return Returns the disciplineNm.
	 */
	public String getDisciplineNm() {
		return disciplineNm;
	}
	/**
	 * @param disciplineNm The disciplineNm to set.
	 */
	public void setDisciplineNm(String disciplineNm) {
		this.disciplineNm = disciplineNm;
	}
	/**
	 * @return Returns the staffTypeCd.
	 */
	public Integer getStaffTypeCd() {
		return staffTypeCd;
	}
	/**
	 * @param staffTypeCd The staffTypeCd to set.
	 */
	public void setStaffTypeCd(Integer staffTypeCd) {
		this.staffTypeCd = staffTypeCd;
	}
	/**
	 * @return Returns the status.
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status The status to set.
	 */
	public void setStatus(String status) {
		this.status = status;
	}

	
	
	

}
